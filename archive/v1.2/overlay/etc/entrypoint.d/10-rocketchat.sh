#!/usr/bin/env bash

declare -x ROCKETCHAT_ROOT_URL
[[ -z "${ROCKETCHAT_ROOT_URL}" ]] && ROCKETCHAT_ROOT_URL="http://localhost:3000"

declare -x ROCKETCHAT_BASE_PATH
[[ -z "${ROCKETCHAT_BASE_PATH}" ]] && ROCKETCHAT_BASE_PATH="/var/lib/rocketchat"

declare -x ROCKETCHAT_AVATARS_PATH
[[ -z "${ROCKETCHAT_AVATARS_PATH}" ]] && ROCKETCHAT_AVATARS_PATH="${ROCKETCHAT_BASE_PATH}/avatars"

declare -x ROCKETCHAT_HEALTHCHECK_URL
[[ -z "${ROCKETCHAT_HEALTHCHECK_URL}" ]] && ROCKETCHAT_HEALTHCHECK_URL="http://localhost:3000"

declare -x ROCKETCHAT_HEALTHCHECK_CODE
[[ -z "${ROCKETCHAT_HEALTHCHECK_CODE}" ]] && ROCKETCHAT_HEALTHCHECK_CODE="200"

declare -x ROCKETCHAT_SKIP_CHOWN
[[ -z "${ROCKETCHAT_SKIP_CHOWN}" ]] && ROCKETCHAT_SKIP_CHOWN="false"
